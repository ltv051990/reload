int	ft_count_if(char **tab, int (*f)(char*))
{
	int line;
	int numb;

	line = 0;
	numb = 0;
	while (tab[line] != '\0')
	{
		if (f(tab[line]))
			numb++;
		line++;
	}
	return (numb);
}
